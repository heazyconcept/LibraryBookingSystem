        }

        public static string GenerateComplexPassword(int length = GeneralConstant.PASSWORD_LENGTH_MIN)
        {
            StringBuilder characterSet = new StringBuilder();
            characterSet.Append(GeneralConstant.LOWERCASE_CHARACTERS);
            characterSet.Append(GeneralConstant.UPPERCASE_CHARACTERS);
            characterSet.Append(GeneralConstant.NUMERIC_CHARACTERS);
            characterSet.Append(GeneralConstant.SPECIAL_CHARACTERS);
            var charaterString = characterSet.ToString();

            const int MAXIMUM_IDENTICAL_CONSECUTIVE_CHARS = 2;
            char[] password = new char[length];
            int characterSetLength = charaterString.Length;
            Random random = new Random();
            for (int characterPosition = 0; characterPosition < length; characterPosition++)
            {
                password[characterPosition] = charaterString[random.Next(characterSetLength - 1)];
                bool moreThanTwoIdenticalInARow =
                characterPosition > MAXIMUM_IDENTICAL_CONSECUTIVE_CHARS
                    && password[characterPosition] == password[characterPosition - 1]
                    && password[characterPosition - 1] == password[characterPosition - 2];
                if (moreThanTwoIdenticalInARow)
                {
                    characterPosition--;
                }
            }
            return string.Join(null, password);

        }

