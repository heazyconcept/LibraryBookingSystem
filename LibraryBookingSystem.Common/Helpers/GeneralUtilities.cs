        public static string Encrypt(string TextToBeEncrypted, string Key = "")
        {

            RijndaelManaged RijndaelCipher = new RijndaelManaged();
            string Password = string.IsNullOrWhiteSpace(Key) ? GeneralConstant.accessKey : Key;
            TextToBeEncrypted = TextToBeEncrypted.Replace(" ", "+");
            byte[] PlainText = Encoding.Unicode.GetBytes(TextToBeEncrypted);
            byte[] Salt = Encoding.ASCII.GetBytes(Password.Length.ToString());
            PasswordDeriveBytes SecretKey = new PasswordDeriveBytes(Password, Salt);
            //Creates a symmetric encryptor object.
            ICryptoTransform Encryptor = RijndaelCipher.CreateEncryptor(SecretKey.GetBytes(32), SecretKey.GetBytes(16));
            MemoryStream memoryStream = new MemoryStream();
            //Defines a stream that links data streams to cryptographic transformations 
            CryptoStream cryptoStream = new CryptoStream(memoryStream, Encryptor, CryptoStreamMode.Write);
            cryptoStream.Write(PlainText, 0, PlainText.Length);
            //Writes the final state and clears the buffer 
            cryptoStream.FlushFinalBlock();
            byte[] CipherBytes = memoryStream.ToArray();
            memoryStream.Close();
            cryptoStream.Close();
            string EncryptedData = Convert.ToBase64String(CipherBytes);
            return EncryptedData;

        }

        public static string Decrypt(string TextToBeDecrypted, string Key = "")
        {

            RijndaelManaged RijndaelCipher = new RijndaelManaged();
            string Password = string.IsNullOrWhiteSpace(Key) ? GeneralConstant.accessKey : Key;
            TextToBeDecrypted = TextToBeDecrypted.Replace(" ", "+");
            byte[] EncryptedData = Convert.FromBase64String(TextToBeDecrypted);
            byte[] Salt = Encoding.ASCII.GetBytes(Password.Length.ToString());
            //Making of the key for decryption 
            PasswordDeriveBytes SecretKey = new PasswordDeriveBytes(Password, Salt);
            //Creates a symmetric Rijndael decryptor object. 
            ICryptoTransform Decryptor = RijndaelCipher.CreateDecryptor(SecretKey.GetBytes(32), SecretKey.GetBytes(16));
            MemoryStream memoryStream = new MemoryStream(EncryptedData);
            //Defines the cryptographics stream for decryption.THe stream contains decrpted data 
            CryptoStream cryptoStream = new CryptoStream(memoryStream, Decryptor, CryptoStreamMode.Read);
            byte[] PlainText = new byte[EncryptedData.Length];
            int DecryptedCount = 0;
            while (DecryptedCount < PlainText.Length)
            {
                int bytesRead = cryptoStream.Read(PlainText, DecryptedCount, PlainText.Length - DecryptedCount);
                if (bytesRead == 0) break;
                DecryptedCount += bytesRead;
            }
            memoryStream.Close();
            cryptoStream.Close();
            //Converting to string 
            string DecryptedData = Encoding.Unicode.GetString(PlainText, 0, DecryptedCount);

            return DecryptedData;

        }

        public static string GenerateOtp(string env = "")
        {
            if (env == "dev") return "112233";
            string otp = "";
            var buffer = new byte[sizeof(ulong)];
            using (var rngCsp = new RNGCryptoServiceProvider())
            {
                rngCsp.GetBytes(buffer);
                var num = BitConverter.ToUInt64(buffer, 0);
                otp = (num % 1000000).ToString("D6");
            }
            return otp;
        }
